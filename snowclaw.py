# Statements from spcs/image-repo.sql, split for REST API (no USE SCHEMA).
SETUP_STATEMENTS = [
    "CREATE DATABASE IF NOT EXISTS snowclaw_db",
    "CREATE SCHEMA IF NOT EXISTS snowclaw_db.snowclaw_schema",
    "CREATE IMAGE REPOSITORY IF NOT EXISTS snowclaw_db.snowclaw_schema.snowclaw_repo",
    (
        "CREATE STAGE IF NOT EXISTS snowclaw_db.snowclaw_schema.snowclaw_state_stage "
        "ENCRYPTION = (TYPE = 'SNOWFLAKE_SSE') DIRECTORY = (ENABLE = TRUE)"
    ),
    (
        "CREATE SECRET IF NOT EXISTS snowclaw_db.snowclaw_schema.snowclaw_secrets "
        "TYPE = GENERIC_STRING SECRET_STRING = '{}'"
    ),
    (
        "CREATE OR REPLACE NETWORK RULE snowclaw_db.snowclaw_schema.snowclaw_egress_rule "
        "MODE = EGRESS TYPE = HOST_PORT VALUE_LIST = ("
        "'openrouter.ai:443', 'api.slack.com:443', "
        "'wss-primary.slack.com:443', 'wss-backup.slack.com:443', "
        "'*.snowflakecomputing.com:443')"
    ),
    (
        "CREATE OR REPLACE EXTERNAL ACCESS INTEGRATION snowclaw_external_access "
        "ALLOWED_NETWORK_RULES = (snowclaw_db.snowclaw_schema.snowclaw_egress_rule) "
        "ENABLED = TRUE"
    ),
    (
        "CREATE COMPUTE POOL IF NOT EXISTS snowclaw_pool "
        "MIN_NODES = 1 MAX_NODES = 1 INSTANCE_FAMILY = CPU_X64_S"
    ),
]


def build_secrets_json(settings: dict) -> str:
    """Build the JSON secret string for snowclaw_secrets."""
    secrets = {
        "snowflake_token": settings.get("pat", ""),
    }
    if settings.get("openrouter_key"):
        secrets["openrouter_api_key"] = settings["openrouter_key"]
    if settings.get("slack_bot_token"):
        secrets["slack_bot_token"] = settings["slack_bot_token"]
    if settings.get("slack_app_token"):
        secrets["slack_app_token"] = settings["slack_app_token"]
    return json.dumps(secrets)


def run_snowflake_setup(settings: dict):
    """Create Snowflake objects via REST API."""
    account = settings["account"]
    pat = settings["pat"]

    with console.status("[bold cyan]Creating Snowflake objects..."):
        for stmt in SETUP_STATEMENTS:
            # Inject actual secret values into the CREATE SECRET statement
            if "snowclaw_secrets" in stmt and "SECRET_STRING" in stmt:
                secrets_json = build_secrets_json(settings)
                stmt = (
                    "CREATE OR REPLACE SECRET snowclaw_db.snowclaw_schema.snowclaw_secrets "
                    f"TYPE = GENERIC_STRING SECRET_STRING = '{secrets_json}'"
                )
            try:
                snowflake_rest_execute(account, pat, stmt)
                # Show short description of what was created
                obj_match = re.search(r"(CREATE\s+\w+(?:\s+\w+)*\s+(?:IF NOT EXISTS\s+)?)([\w.]+)", stmt, re.IGNORECASE)
                label = obj_match.group(2) if obj_match else stmt[:60]
                console.print(f"  [green]✓[/green] {label}")
            except requests.HTTPError as e:
                console.print(f"  [red]✗[/red] Failed: {stmt[:80]}...")
                console.print(f"    [dim]{e}[/dim]")
                raise


