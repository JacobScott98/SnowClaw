def find_project_root() -> Path:
    """Walk up from cwd to find the SnowClaw project root (contains Dockerfile + config/)."""
    cur = Path.cwd()
    for d in [cur, *cur.parents]:
        if (d / "Dockerfile").exists() and (d / "config").is_dir():
            return d
    console.print("[red]Could not find SnowClaw project root (no Dockerfile + config/ found).[/red]")
    console.print("Run this command from inside the snowclaw repo.")
    sys.exit(1)


def load_dotenv(path: Path) -> dict[str, str]:
    """Parse a .env file into a dict. Ignores comments and blank lines."""
    env: dict[str, str] = {}
    if not path.exists():
        return env
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" in line:
            key, _, value = line.partition("=")
            env[key.strip()] = value.strip()
    return env


def snowflake_rest_execute(
    account: str,
    pat: str,
    sql: str,
    database: str | None = None,
    schema: str | None = None,
) -> dict:
    """Execute a single SQL statement via the Snowflake REST API."""
    url = f"https://{account}.snowflakecomputing.com/api/v2/statements"
    headers = {
        "Authorization": f"Bearer {pat}",
        "Content-Type": "application/json",
        "Accept": "application/json",
        "X-Snowflake-Authorization-Token-Type": "PROGRAMMATIC_ACCESS_TOKEN",
    }
    body: dict = {"statement": sql, "timeout": 60}
    if database:
        body["database"] = database
    if schema:
        body["schema"] = schema

    resp = requests.post(url, headers=headers, json=body, timeout=30)
    resp.raise_for_status()
    return resp.json()


def render_banner():
    """Print the SnowClaw welcome banner."""
    title = Text("❄  SnowClaw", style="bold cyan")
    subtitle = Text(f"v{__version__} — OpenClaw on Snowflake", style="dim")
    banner = Text.assemble(title, "\n", subtitle)
    console.print(Panel(banner, expand=False, border_style="cyan"))
    console.print()


